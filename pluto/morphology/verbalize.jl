# ╔═╡ 8448bd9b-4924-4bb3-ae55-0c899b5cc479
"""Create user-selectable menu of verbs in the selected corpus,
sorted by frequency."""
function verbsmenu()
	histmenu = Pair{String, String}["" => "--Choose a verb--"]
	for k in keys(verbhisto)
		count = verbhisto[k]
		id = split(k, "@")[1]
		display = count == 1 ? string(k, " (", count, " passage)")  : string(k, " (", count, " passages)")
		push!(histmenu, id => display)
		
		
	end
	histmenu
end

